package com.smile.box.app.adapter;
import android.graphics.Bitmap;

public class Web
{
	private String title;
	private Bitmap icon;


	public void setTitle(String title)
	{
		this.title = title;
	}

	public String getTitle()
	{
		return title;
	}

	public void setIcon(Bitmap icon)
	{
		this.icon = icon;
	}

	public Bitmap getIcon()
	{
		return icon;
	}
}
